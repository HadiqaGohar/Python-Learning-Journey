# Arithmetic Operators

# Example 1 : 

a = 5
b = 10

print(a + b)  # Addition (+)
# Output: 15

print(a - b)  # Subtraction (-)
# Output: -5

print(a * b)  # Multiplication (*)
# Output: 50

print(a / b)  # Division (/)
# Output: 0.5

print(a % b)  # Modulus (%)
# Output: 5  (Not 0.5, as modulus gives the remainder)

print(a ** b)  # Exponentiation (**)
# Output: 9765625 (5^10)

print(a // b)  # Floor Division (//)
# Output: 0  (Not 9765625, because 5 // 10 gives 0)
