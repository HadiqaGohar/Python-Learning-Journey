# Comparison (Relational) Operators in Python

a = 10
b = 5

# Equal to (==)

print ( a == b ) # Equal to (==)
# Output : False

print ( a != b ) # Not Equal to (!=)
# Output : True

print ( a > b ) # Greater than (>)
# Output : True

print ( a < b ) # Less than (>)
# Output : False

print ( a >= b ) # Greater than or equal to (>=)
# Output : True

print ( a <= b ) # Less than or equal to (>=)
# Output : False