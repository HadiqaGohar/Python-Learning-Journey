# Variables in Python!

### Example 1:

# Define variables
name = "Hadiqa"
age = 18
decimals = 0.5

print(name)
print(age)
print(decimals)

## Output will be:
# Hadiqa  
# 18
# 0.5

### Example 2:

# Change the value of variable
# Print both variables at the same time

name = "Gohar"
age = 19

print(name, age)

## Output will be:
# Gohar 19

### Example 3:

# Add text with variables
# concatinate variables with text

fullName = "Hadiqa Gohar"
role = "Leader"
rollNo = 420730
student = "GIAIC"

print("My name is", fullName)
